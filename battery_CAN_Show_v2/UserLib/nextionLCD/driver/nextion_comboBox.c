void nextion_comboBox_get_text(const char *id)
{
    // get cb0.txt
    char cmd[50];
    memset(cmd, 0x00, 50);
    uint16_t len = 0;
    len = snprintf(cmd, 50, "get %s.txt", id);
    nextion_send_message(cmd, len);
}
