/**
 * @brief set picture of the button
 *
 * @param name
 * @param pic
 */
void nextion_button_change_pic(const char *name, const uint8_t pic)
{
    // localCell1.pic=8
    char cmd[50];
    memset(cmd, 0x00, 50);
    uint16_t len = 0;
    len = snprintf(cmd, 50, "%s.pic=%d", name, pic);
    nextion_send_message(cmd, len);

    memset(cmd, 0x00, 50);
    len = 0;
    len = snprintf(cmd, 50, "%s.pic2=%d", name, pic);
    nextion_send_message(cmd, len);
}

/**
 * @brief set enable/disable touch button
 * 
 * @param name 
 * @param enabled 
 */
void nextion_button_enable(const char *name, bool enabled)
{
    // tsw bt0,0
    char cmd[50];
    memset(cmd, 0x00, 50);
    uint16_t len = 0;
    len = snprintf(cmd, 50, "tsw %s,%d", name, enabled);
    nextion_send_message(cmd, len);
}
